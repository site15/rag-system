import { ApiProperty } from '@nestjs/swagger';

export class ChatPromptDto {
  @ApiProperty({
    type: 'string',
  })
  id!: string;
  @ApiProperty({
    type: 'string',
    nullable: true,
  })
  key!: string | null;
  @ApiProperty({
    type: 'string',
    nullable: true,
  })
  prompt!: string | null;
  @ApiProperty({
    type: 'string',
    format: 'date-time',
  })
  createdAt!: Date;
  @ApiProperty({
    type: 'string',
    format: 'date-time',
  })
  updatedAt!: Date;
}
